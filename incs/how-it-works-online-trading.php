                    <h3>Buyers & sellers</h3>
                </div>
                <div class="bottom">
                    <p>A stock market like the ASX brings together people who want to sell their shares, and people who want to buy them. Each stock will have a list of orders in the 'bid' and 'offer' columns, indicating the the prices which buyers and sellers are willing to pay/accept for their shares and the quantity which can be bought or sold for that price.</p>
                </div>
            </div>
            
            <div class="column">
                <div class="top">
                    <h3>Orders</h3>
                </div>
                <div class="bottom">
                    <p>To buy or sell a particular stock, an investor must place and order. The order can be to sell or to buy, and will include the code for the stock to be transacted, the quantity and the price. The price can be 'at market', which is the current market price for the share, or it can be 'at limit', which will only execute the order once the share price crosses the limit.</p>
                </div>
            </div>
            
            <div class="column">
                <div class="top">
                    <h3>Contract notes</h3>
                </div>
                <div class="bottom">
                    <p>Once an order has been executed, the investor receives a conformation called a 'contract note' from the broker which will include the details of the transaction.</p>
                </div>
            </div>
            
            <div class="column">
                <div class="top">
                    <h3>Trade settlement</h3>
                </div>
                <div class="bottom">
                    <p>Shares aren't exchanged or paid for the instant the trade occurs. Instead, the transaction occurs several days after the trade. Most modern stock exchanges have a T+2 settlement system, which means the transaction occurs two business days after the trade is executed.   If you have sold some stock we allow you to trade on the unsettled funds, meaning your cash isn't tied up in the settlement process.</p>
                </div>
